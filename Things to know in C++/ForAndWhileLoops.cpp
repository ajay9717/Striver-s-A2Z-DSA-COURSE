// For, while and do while loops.
#include<bits/stdc++.h>
using namespace std;

int main()
{
    // For loop
    for(int i = 0; i < 5; i++)
    {
        cout << "Hello world" << endl;
    }

    // While loop
    int j = 0;
    while(j < 5)
    {
        cout << "Hello World!" << endl;
        j++;
    }

    //Do while loop
    int k = 0;
    do{
        cout << "Hello World." << endl;
        k++;
    }while(k < 5);
    
    
}